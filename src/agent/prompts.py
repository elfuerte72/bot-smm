def build_user_prompt(*, exclude_urls: list[str]) -> str:
    excluded = "\n".join(f"- {u}" for u in exclude_urls) if exclude_urls else "(пусто)"
    return f"""\
Найди один свежий инфоповод по заданной тематике и подготовь пост.

УЖЕ ОПУБЛИКОВАННЫЕ или РАССМОТРЕННЫЕ источники — НЕ бери эти URL и не пиши пост по ним:
{excluded}

Используй web_search для поиска и проверки фактов. После работы инструмента
верни итог СТРОГО в виде JSON-объекта (без markdown, без комментариев).
"""
